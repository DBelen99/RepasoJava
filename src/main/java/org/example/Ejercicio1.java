package org.example;

public class Ejercicio1 {
    //Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás.
    public static void main(String[] args) {

        int numResta=320;

        while(numResta>=160){
            System.out.println(numResta);
            numResta-=20;
        }
    }


}
